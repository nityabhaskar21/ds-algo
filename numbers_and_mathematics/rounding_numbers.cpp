#include <iostream>
#include <cmath>
using namespace std;
 
// Driver function
int main()
{
    // using floor function which return
    // floor of input value
    cout << "Floor is : " << floor(2.3) << endl;
    cout << "Floor is : " << floor(-2.3) << endl;


    // using ceil function which return
    // floor of input value
    cout << "Ceil is : " << ceil(2.3) << endl;
    cout << "Ceil is : " << ceil(-2.3) << endl;


    // using round function which return
    // round of input value
    cout << "Round is : " << round(2.3) << endl;
    cout << "Round is : " << round(-2.3) << endl;
 
    return 0;
}